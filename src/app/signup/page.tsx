'use client';

import Link from 'next/link';
import { useState, Suspense } from 'react';
import { useSearchParams } from 'next/navigation';
import { motion } from 'framer-motion';
import { Dumbbell, Mail, Lock, ArrowRight, Eye, EyeOff, Dumbbell as DumbbellIcon, Users } from 'lucide-react';
import { Button } from '@/components/ui/button';
import { Input } from '@/components/ui/input';
import { Label } from '@/components/ui/label';
import { Separator } from '@/components/ui/separator';
import { cn } from '@/lib/utils';

function SignupForm() {
    const searchParams = useSearchParams();
    const defaultRole = searchParams.get('role') || '';

    const [showPassword, setShowPassword] = useState(false);
    const [role, setRole] = useState<'client' | 'coach' | ''>(defaultRole as 'client' | 'coach' | '');
    const [step, setStep] = useState(defaultRole ? 2 : 1);

    const [isLoading, setIsLoading] = useState(false);
    const [error, setError] = useState('');
    const [formData, setFormData] = useState({
        firstName: '',
        lastName: '',
        email: '',
        password: '',
    });

    const handleRegister = async (e: React.FormEvent) => {
        e.preventDefault();
        setError('');
        setIsLoading(true);

        try {
            const res = await fetch('/api/auth/register', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({
                    email: formData.email,
                    password: formData.password,
                    role: role.toUpperCase(),
                    displayName: `${formData.firstName} ${formData.lastName}`.trim(),
                }),
            });

            const data = await res.json();

            if (!res.ok) {
                throw new Error(data.error || 'Registration failed');
            }

            // Redirect on success
            window.location.href = role === 'coach' ? '/dashboard/coach' : '/dashboard';
        } catch (err: any) {
            setError(err.message);
            setIsLoading(false);
        }
    };

    return (
        <div className="flex min-h-screen">
            {/* Left Side - Form */}
            <div className="flex flex-1 flex-col justify-center px-4 py-12 sm:px-6 lg:px-20 xl:px-24">
                <div className="mx-auto w-full max-w-sm">
                    <Link href="/" className="flex items-center gap-2">
                        <div className="flex h-10 w-10 items-center justify-center rounded-xl bg-primary">
                            <Dumbbell className="h-5 w-5 text-primary-foreground" />
                        </div>
                        <span className="text-2xl font-bold">
                            Fit<span className="text-primary">Connect</span>
                        </span>
                    </Link>

                    <h1 className="mt-8 text-2xl font-bold tracking-tight">Create your account</h1>
                    <p className="mt-2 text-muted-foreground">
                        {step === 1 ? 'Choose how you want to use FitConnect' : 'Fill in your details to get started'}
                    </p>

                    {/* Step 1: Role Selection */}
                    {step === 1 && (
                        <motion.div
                            initial={{ opacity: 0, y: 10 }}
                            animate={{ opacity: 1, y: 0 }}
                            className="mt-8 space-y-4"
                        >
                            <button
                                onClick={() => {
                                    setRole('client');
                                    setStep(2);
                                }}
                                className={cn(
                                    'flex w-full items-center gap-4 rounded-2xl border-2 p-6 text-left transition-all hover:border-primary/50',
                                    role === 'client' && 'border-primary bg-primary/5'
                                )}
                            >
                                <div className="flex h-14 w-14 items-center justify-center rounded-xl bg-primary/10">
                                    <Users className="h-7 w-7 text-primary" />
                                </div>
                                <div>
                                    <p className="font-semibold">I'm looking for a trainer</p>
                                    <p className="text-sm text-muted-foreground">
                                        Find coaches, book sessions, and transform your fitness
                                    </p>
                                </div>
                            </button>

                            <button
                                onClick={() => {
                                    setRole('coach');
                                    setStep(2);
                                }}
                                className={cn(
                                    'flex w-full items-center gap-4 rounded-2xl border-2 p-6 text-left transition-all hover:border-primary/50',
                                    role === 'coach' && 'border-primary bg-primary/5'
                                )}
                            >
                                <div className="flex h-14 w-14 items-center justify-center rounded-xl bg-primary/10">
                                    <DumbbellIcon className="h-7 w-7 text-primary" />
                                </div>
                                <div>
                                    <p className="font-semibold">I'm a personal trainer</p>
                                    <p className="text-sm text-muted-foreground">
                                        Grow your business, manage clients, and get paid securely
                                    </p>
                                </div>
                            </button>
                        </motion.div>
                    )}

                    {/* Step 2: Registration Form */}
                    {step === 2 && (
                        <motion.form
                            initial={{ opacity: 0, y: 10 }}
                            animate={{ opacity: 1, y: 0 }}
                            className="mt-8 space-y-5"
                            onSubmit={handleRegister}
                        >
                            {error && (
                                <div className="rounded-lg bg-destructive/10 p-3 text-sm text-destructive">
                                    {error}
                                </div>
                            )}

                            <div className="grid grid-cols-2 gap-4">
                                <div>
                                    <Label htmlFor="firstName">First name</Label>
                                    <Input
                                        id="firstName"
                                        placeholder="John"
                                        className="mt-2"
                                        value={formData.firstName}
                                        onChange={(e) => setFormData({ ...formData, firstName: e.target.value })}
                                        required
                                    />
                                </div>
                                <div>
                                    <Label htmlFor="lastName">Last name</Label>
                                    <Input
                                        id="lastName"
                                        placeholder="Doe"
                                        className="mt-2"
                                        value={formData.lastName}
                                        onChange={(e) => setFormData({ ...formData, lastName: e.target.value })}
                                        required
                                    />
                                </div>
                            </div>

                            <div>
                                <Label htmlFor="email">Email address</Label>
                                <div className="relative mt-2">
                                    <Mail className="absolute left-3 top-1/2 h-4 w-4 -translate-y-1/2 text-muted-foreground" />
                                    <Input
                                        id="email"
                                        type="email"
                                        placeholder="you@example.com"
                                        className="pl-10"
                                        value={formData.email}
                                        onChange={(e) => setFormData({ ...formData, email: e.target.value })}
                                        required
                                    />
                                </div>
                            </div>

                            <div>
                                <Label htmlFor="password">Password</Label>
                                <div className="relative mt-2">
                                    <Lock className="absolute left-3 top-1/2 h-4 w-4 -translate-y-1/2 text-muted-foreground" />
                                    <Input
                                        id="password"
                                        type={showPassword ? 'text' : 'password'}
                                        placeholder="••••••••"
                                        className="pl-10 pr-10"
                                        value={formData.password}
                                        onChange={(e) => setFormData({ ...formData, password: e.target.value })}
                                        required
                                        minLength={8}
                                    />
                                    <button
                                        type="button"
                                        onClick={() => setShowPassword(!showPassword)}
                                        className="absolute right-3 top-1/2 -translate-y-1/2 text-muted-foreground hover:text-foreground"
                                    >
                                        {showPassword ? <EyeOff className="h-4 w-4" /> : <Eye className="h-4 w-4" />}
                                    </button>
                                </div>
                                <p className="mt-2 text-xs text-muted-foreground">
                                    Must be at least 8 characters
                                </p>
                            </div>

                            <div className="flex items-start gap-2">
                                <input type="checkbox" id="terms" required className="mt-1 rounded border-muted-foreground" />
                                <Label htmlFor="terms" className="text-sm font-normal text-muted-foreground">
                                    I agree to the{' '}
                                    <Link href="/terms" className="text-primary hover:underline">
                                        Terms of Service
                                    </Link>{' '}
                                    and{' '}
                                    <Link href="/privacy" className="text-primary hover:underline">
                                        Privacy Policy
                                    </Link>
                                </Label>
                            </div>

                            <Button type="submit" className="w-full rounded-xl" size="lg" disabled={isLoading}>
                                {isLoading ? 'Creating account...' : (
                                    <>
                                        Create account
                                        <ArrowRight className="ml-2 h-4 w-4" />
                                    </>
                                )}
                            </Button>

                            <Button
                                type="button"
                                variant="ghost"
                                className="w-full"
                                onClick={() => setStep(1)}
                                disabled={isLoading}
                            >
                                ← Back to role selection
                            </Button>
                        </motion.form>
                    )}

                    {step === 2 && (
                        <>
                            <div className="mt-6">
                                <div className="relative">
                                    <div className="absolute inset-0 flex items-center">
                                        <Separator />
                                    </div>
                                    <div className="relative flex justify-center text-xs uppercase">
                                        <span className="bg-background px-2 text-muted-foreground">
                                            Or continue with
                                        </span>
                                    </div>
                                </div>

                                <div className="mt-6 grid grid-cols-2 gap-3">
                                    <Button variant="outline" className="rounded-xl" type="button">
                                        <svg className="mr-2 h-4 w-4" viewBox="0 0 24 24">
                                            <path
                                                fill="currentColor"
                                                d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                                            />
                                            <path
                                                fill="currentColor"
                                                d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                                            />
                                            <path
                                                fill="currentColor"
                                                d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                                            />
                                            <path
                                                fill="currentColor"
                                                d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                                            />
                                        </svg>
                                        Google
                                    </Button>
                                    <Button variant="outline" className="rounded-xl" type="button">
                                        <svg className="mr-2 h-4 w-4" fill="currentColor" viewBox="0 0 24 24">
                                            <path d="M12 0c-6.626 0-12 5.373-12 12 0 5.302 3.438 9.8 8.207 11.387.599.111.793-.261.793-.577v-2.234c-3.338.726-4.033-1.416-4.033-1.416-.546-1.387-1.333-1.756-1.333-1.756-1.089-.745.083-.729.083-.729 1.205.084 1.839 1.237 1.839 1.237 1.07 1.834 2.807 1.304 3.492.997.107-.775.418-1.305.762-1.604-2.665-.305-5.467-1.334-5.467-5.931 0-1.311.469-2.381 1.236-3.221-.124-.303-.535-1.524.117-3.176 0 0 1.008-.322 3.301 1.23.957-.266 1.983-.399 3.003-.404 1.02.005 2.047.138 3.006.404 2.291-1.552 3.297-1.23 3.297-1.23.653 1.653.242 2.874.118 3.176.77.84 1.235 1.911 1.235 3.221 0 4.609-2.807 5.624-5.479 5.921.43.372.823 1.102.823 2.222v3.293c0 .319.192.694.801.576 4.765-1.589 8.199-6.086 8.199-11.386 0-6.627-5.373-12-12-12z" />
                                        </svg>
                                        GitHub
                                    </Button>
                                </div>
                            </div>
                        </>
                    )}

                    <p className="mt-8 text-center text-sm text-muted-foreground">
                        Already have an account?{' '}
                        <Link href="/login" className="text-primary hover:underline">
                            Sign in
                        </Link>
                    </p>
                </div>
            </div>

            {/* Right Side - Hero Image */}
            <div className="relative hidden flex-1 lg:block">
                <div className="absolute inset-0 bg-gradient-to-br from-primary/20 via-primary/10 to-accent/20" />
                <motion.div
                    initial={{ opacity: 0, x: 20 }}
                    animate={{ opacity: 1, x: 0 }}
                    transition={{ delay: 0.2 }}
                    className="relative flex h-full flex-col justify-center px-12"
                >
                    <div className="max-w-md">
                        <h2 className="text-3xl font-bold">
                            {role === 'coach'
                                ? 'Grow your training business'
                                : 'Transform your fitness journey'}
                        </h2>
                        <ul className="mt-8 space-y-4">
                            {(role === 'coach'
                                ? [
                                    'Set your own rates and schedule',
                                    'Get paid securely via Stripe',
                                    'Manage clients in one place',
                                    'Build your professional profile',
                                ]
                                : [
                                    'Find verified personal trainers',
                                    'Book sessions with one click',
                                    'Pay securely through the platform',
                                    'Track your progress and goals',
                                ]
                            ).map((item, i) => (
                                <li key={i} className="flex items-center gap-3">
                                    <div className="flex h-6 w-6 items-center justify-center rounded-full bg-primary/20 text-primary">
                                        ✓
                                    </div>
                                    <span>{item}</span>
                                </li>
                            ))}
                        </ul>
                    </div>
                </motion.div>
            </div>
        </div>
    );
}

export default function SignupPage() {
    return (
        <Suspense fallback={<div className="flex min-h-screen items-center justify-center">Loading...</div>}>
            <SignupForm />
        </Suspense>
    );
}
